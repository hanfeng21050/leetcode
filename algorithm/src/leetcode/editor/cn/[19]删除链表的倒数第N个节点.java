/**
 * Definition for singly-linked list.
 * public class ListNode {
 *     int val;
 *     ListNode next;
 *     ListNode(int x) { val = x; }
 * }
 */
 class Solution {
    public ListNode removeNthFromEnd(ListNode head, int n) {
        ListNode pre = head;
        ListNode sub = head;
        ListNode tmp = new ListNode(-1);
        tmp.next = head;
        while (n > 1) {
            sub = sub.next;
            n--;
        }
        if(sub.next == null) {
            return head.next;
        }
        while (sub.next != null) {
            sub = sub.next;
            pre = pre.next;
            tmp = tmp.next;
        }
        if(sub.next == null && pre.next == null) {
            tmp.next = null;
        }else {
            tmp.next = pre.next;
        }
        return head;
    }
}
